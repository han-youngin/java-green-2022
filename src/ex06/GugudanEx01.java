package ex06;

public class GugudanEx01 {
    public static void main(String[] args) {

        int step;
        // 2단을 출력하는 프로그램 만들어주라
        step = 2;
        System.out.println(step + "*1=2");
        System.out.println(step + "*2=4");
        System.out.println(step + "*3=6");
        System.out.println(step + "*4=8");
        System.out.println(step + "*5=10");
        System.out.println(step + "*6=12");
        System.out.println(step + "*7=14");
        System.out.println(step + "*8=16");
        System.out.println(step + "*9=18");

        // 3단 추가좀

        // 4단
        System.out.println("4*1=4");
        System.out.println("4*2=8");
        System.out.println("4*3=12");
        System.out.println("4*4=16");
        System.out.println("4*5=20");
        System.out.println("4*6=24");
        System.out.println("4*7=28");
        System.out.println("4*8=32");
        System.out.println("4*9=36");

        // 5
        step = 5;
        System.out.println(step + "*1=" + (step * 1));
        System.out.println(step + "*2=" + (step * 2));
        System.out.println(step + "*3=" + (step * 3));
        System.out.println(step + "*4=" + (step * 4));
        System.out.println(step + "*5=" + (step * 5));
        System.out.println(step + "*6=" + (step * 6));
        System.out.println(step + "*7=" + (step * 7));
        System.out.println(step + "*8=" + (step * 8));
        System.out.println(step + "*9=" + (step * 9));
    }
}